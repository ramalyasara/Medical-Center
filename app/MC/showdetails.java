    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DBconnecter db = new DBconnecter();

        String pName = jTextField2.getText();

        try {
            Connection con = db.getConnection();
            Statement stmt = con.createStatement();

            ResultSet rs = stmt.executeQuery("SELECT * FROM patient where pName='" + pName + "'");

            while (rs.next()) {
                String pId = rs.getString("pId");
                String registerNum = rs.getString("registerNum");
                String type = rs.getString("type");
                String gender = rs.getString("gender");
                String address = rs.getString("address");
                String telNum = rs.getString("telNum");
                String allagies = rs.getString("allagies");
                String bloodType = rs.getString("bloodType");

                jTextField1.setText(pId);
                jTextField3.setText(registerNum);
                jTextField4.setText(type);
                jTextField5.setText(gender);
                jTextField6.setText(bloodType);
                jTextField7.setText(telNum);
                jTextArea1.setText(address);
                jTextArea2.setText(allagies);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

